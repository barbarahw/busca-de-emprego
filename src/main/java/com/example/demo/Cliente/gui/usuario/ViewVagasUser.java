    private void carregarTabela(JSONArray vagas) {
        String[] colunas = {
            "ID", "Título", "Área", "Empresa", "Descricao", "Localização", "Salário", "Contato"
        };

        DefaultTableModel modelo = new DefaultTableModel(colunas, 0);

        for (int i = 0; i < vagas.length(); i++) {
            JSONObject vaga = vagas.getJSONObject(i);

            String local = vaga.optString("city") + "/" + vaga.optString("state");

            modelo.addRow(new Object[]{
                vaga.optInt("job_id"),
                vaga.optString("title"),
                vaga.optString("area"),
                vaga.optString("company"),
                vaga.optString("description"),
                local,
                vaga.optDouble("salary"),
                vaga.opt("contact")
            });
        }
        tabelaVagas.setModel(modelo);
    }
