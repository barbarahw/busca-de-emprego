    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            JSONObject filtros= new JSONObject();
            
            if (!txtTitulo.getText().trim().isEmpty()) {
                filtros.put("title", txtTitulo.getText().trim());
            }

            if (cbArea.getSelectedItem() != null && !cbArea.getSelectedItem().toString().isEmpty()) {
                filtros.put("area", cbArea.getSelectedItem().toString());
            }
            
            if (!txtEmpresa.getText().trim().isEmpty()) {
                filtros.put("company", txtEmpresa.getText().trim());
            }

            if (!txtCidade.getText().trim().isEmpty()) {
                filtros.put("city", txtCidade.getText().trim());
            }

            if (cbEstado.getSelectedItem() != null && !cbEstado.getSelectedItem().toString().isEmpty()) {
                filtros.put("state", cbEstado.getSelectedItem().toString());
            }
            
            JSONObject salaryRange = new JSONObject();
            boolean hasSalary = false;
            
            if (cbSalMin.getSelectedItem() != null && !cbSalMin.getSelectedItem().toString().isEmpty()) {
                salaryRange.put("min", (Double) Double.parseDouble(cbSalMin.getSelectedItem().toString()));
            }
            
            if (cbSalMax.getSelectedItem() != null && !cbSalMax.getSelectedItem().toString().isEmpty()) {
                salaryRange.put("max", (Double) Double.parseDouble(cbSalMax.getSelectedItem().toString()));
            }
            
            if (cbSalMin.getSelectedItem() != null && cbSalMax.getSelectedItem() != null) {
                hasSalary = true;
            }
            
            if (hasSalary) {
                filtros.put("salary_range", salaryRange);
            }
            
            JSONObject requestJson = new JSONObject();
            requestJson.put("filters", new JSONArray().put(filtros));
            
            String jsonFiltros = requestJson.toString();
            
            HttpResponse<String> response = clienteHttp.buscarVagas(token, jsonFiltros);
            
            JSONObject json = new JSONObject(response.body());
            
            JSONArray itens;
            if (json.has("items")) {
                itens = json.getJSONArray("items");
            } else {
                itens = new JSONArray(); 
            }
            
            carregarTabela(itens);
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao buscar vags: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

